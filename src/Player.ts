////////////////////////////////////////////////////////////////////////////////
///	@class		Player
///	@brief		プレイヤークラス
///
////////////////////////////////////////////////////////////////////////////////
class Player
{
	private cards: Card[];								//!< プレイヤーカード
	private cardsCnt: number;							//!< プレイヤーカード枚数
	private score: number;								//!< プレイヤースコア

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			コンストラクタ
	///	@fn				public constructor()
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public constructor()
	{
		this.cards = new Array();
		this.cardsCnt = 0;
		this.score = 0;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			セッター
	///	@fn				public setCards(cards: Card[]): void
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public setCards(cards: Card[]): void
	{
		this.cards = cards;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			ゲッター
	///	@fn				public getCards(): Card[]
	///	@return			プレイヤーカード
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public getCards(): Card[]
	{
		return this.cards;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			セッター
	///	@fn				public setCardsCnt(cardsCnt: number): void
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public setCardsCnt(cardsCnt: number): void
	{
		this.cardsCnt = cardsCnt;
	}
	
	////////////////////////////////////////////////////////////////////////////////
	///	@brief			ゲッター
	///	@fn				public getCardsCnt(): number
	///	@return			プレイヤーカード枚数
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public getCardsCnt(): number
	{
		return this.cardsCnt;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			セッター
	///	@fn				public setScore(score: number): void
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public setScore(score: number): void
	{
		this.score = score;
	}
	
	////////////////////////////////////////////////////////////////////////////////
	///	@brief			ゲッター
	///	@fn				public getScore(): number
	///	@return			プレイヤーカード枚数
	///	@author			Yuta Yoshinaga
	///	@date			2018.05.04
	///
	////////////////////////////////////////////////////////////////////////////////
	public getScore(): number
	{
		return this.score;
	}
}
